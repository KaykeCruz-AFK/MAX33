class ProdutoTransporte:
    def __init__(self, codigo, nome, categoria, preco):
        self.codigo = codigo
        self.nome = nome
        self.categoria = categoria  # ex: Peça, Serviço, Equipamento
        self.preco = preco

    def __str__(self):
        return (f"Código: {self.codigo} | Nome: {self.nome} | "
                f"Categoria: {self.categoria} | Preço: R$ {self.preco:.2f}")

class EstoqueTransporte:
    def __init__(self):
        self.produtos = []

    def adicionar_produto(self):
        codigo = input("Digite o código do produto: ")
        if any(p.codigo == codigo for p in self.produtos):
            print("Produto com este código já existe!")
            return
        nome = input("Digite o nome do produto: ")
        categoria = input("Digite a categoria (Peça, Serviço, Equipamento): ")
        try:
            preco = float(input("Digite o preço do produto: "))
        except ValueError:
            print("Preço inválido!")
            return

        produto = ProdutoTransporte(codigo, nome, categoria, preco)
        self.produtos.append(produto)
        print("Produto adicionado com sucesso!")

    def listar_produtos(self):
        if not self.produtos:
            print("Nenhum produto cadastrado.")
            return

        print("\nLista de Produtos da Área de Transportes:")
        for produto in self.produtos:
            print(produto)

    def buscar_produto(self):
        codigo = input("Digite o código do produto para buscar: ")
        for produto in self.produtos:
            if produto.codigo == codigo:
                print("Produto encontrado:")
                print(produto)
                return
        print("Produto não encontrado.")

if __name__ == "__main__":
    estoque = EstoqueTransporte()

    while True:
        print("\n1. Adicionar produto")
        print("2. Listar produtos")
        print("3. Buscar produto")
        print("4. Sair")
        escolha = input("Escolha uma opção: ")

        if escolha == "1":
            estoque.adicionar_produto()
        elif escolha == "2":
            estoque.listar_produtos()
        elif escolha == "3":
            estoque.buscar_produto()
        elif escolha == "4":
            print("Encerrando o programa.")
            break
        else:
            print("Opção inválida. Tente novamente.")